			<h2>  Complications  </h2>
	<p>  <i><center> "Diarrhea can be nothing to worry about, or it can be potentially life-threatening," said Lustbader, who explained that the underlying cause of a patient's diarrhea is what determines the seriousness of this uncomfortable ailment.  
The primary complication of diarrhea is dehydration caused by the loss of large amounts of water, salt and nutrients. According to the Mayo Clinic, dehydration can lead to other serious conditions such as low blood pressure, seizures, kidney failure or even death. Those with ongoing diarrhea should seek medical attention if they experience:  </i></center></p>
<li>	Dark urine or small amounts of urine
<li>	Rapid heart rate
<li>	Dry, flushed skin
<li>	Headaches or light-headedness
<li>	Fatigue
<li>	Irritability or confusion
<li>	Severe abdominal or rectal pain
<li>	Blood in the stool or black, tar-like stools


</p>

<h2>  Types of diarrhea </h2>
<p><i><center>
A bout of diarrhea that lasts no more than two weeks is referred to as acute diarrhea and is most often caused by a viral infection, according to the ACG. The most common diarrhea-causing virus for adults is norovirus, which is often referred to as "cruise ship diarrhea" due to its unfortunate tendency to infect sea-faring vacationers. Rotavirus, another diarrhea-inducing virus, is very common in young children.</p>
 <p>  Other causes of acute diarrhea include bacterial infection, which is often referred to as "traveler's diarrhea," or, in some parts of the world, "Montezuma's revenge." But those who come down with this uncomfortable ailment aren't the victims of an ancient curse; they're usually the victims of the bacteria enterotoxigenic Escherichia coli (ETEC), according to Dr. Ian Lustbader, a clinical associate professor of medicine and a gastroenterologist at New York University's Langone Medical Center.</p>
