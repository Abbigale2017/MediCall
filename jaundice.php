            <h2>Symptoms of jaundice </h2>
            <p ><li>	Yellow tinge to the skin and the whites of the eyes, normally starting at the head and spreading down the body
<li>	Pruritis (itchiness)
<li>	Fatigue
<li>	Abdominal pain - typically indicates a blockage of the bile duct
<li>	Weight loss
<li>	Vomiting
<li>	Fever
<li>	Dark urine


</p> 
            <br />

            <h2>Types of jaundice</h2>
            <p >There are three main types of jaundice: 
<li>	Hepatocellular jaundice - occurs as a result of liver disease or injury
<li>	Hemolytic jaundice - occurs as a result of hemolysis (an accelerated breakdown of red blood cells) leading to an increase in production of bilirubin
<li>        Obstructive jaundice - occurs as a result of an obstruction in the bile duct (a system of tubes that carries bile from the liver to the gallbladder and small intestine), which prevents bilirubin from leaving the liver
Jaundice, not to be confused with infant jaundice, is usually a sign of an underlying disorder.


</p> 
