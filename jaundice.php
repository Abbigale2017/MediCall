	            <h1>Causes of jaundice</h1>
            </div>
            <p>Jaundice most often occurs as a result of an underlying disorder that either causes overproduction of bilirubin or prevents the liver from disposing of it, both of which result in bilirubin being deposited in tissues. 
Some underlying conditions that may cause jaundice are: 
<li>	Acute inflammation of the liver - may impair the ability of the liver to conjugate and secrete bilirubin, resulting in a buildup.
<li>	Inflammation of the bile duct - may prevent the secretion of bile and removal of bilirubin, causing jaundice.
<li>	Obstruction of the bile duct - prevents the liver from disposing of bilirubin.
<li>	Hemolytic anemia - production of bilirubin increases when large quantities of red blood cells are broken down.
<li>	Gilbert's syndrome - an inherited condition that impairs the ability of enzymes to process the excretion of bile.
<li>	Cholestasis - a condition where the flow of bile from the liver is interrupted. The bile containing conjugated bilirubin remains in the liver instead of being excreted.
More rare conditions that may cause jaundice include: 
<li>	Crigler-Najjar syndrome - an inherited condition that impairs the specific enzyme responsible for processing bilirubin
<li>	Dubin-Johnson syndrome - an inherited form of chronic jaundice that prevents conjugated bilirubin from being secreted out of the liver's cells
<li>	Pseudojaundice - a harmless form of jaundice in which the yellowing of the skin results from an excess of beta-carotene, not from an excess of bilirubin; usually from eating lots of carrots, pumpkin, or melon




</p>          
