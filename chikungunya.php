            <h2>How is it transmitted? </h2>
            <p >It is transferred from human to human by mosquitoes. The females of Aedes Aegyptii and Aedes albopictus are the main species of mosquito that spread the disease. These mosquitoes bite through the day.
Chikungunya is an infection in humans caused by the chikungunya virus.
The chikungunya virus is present in Africa, southeast Asia, the Indian subcontinent and Indian Ocean islands, where a number of outbreaks have occurred.

</p> 
